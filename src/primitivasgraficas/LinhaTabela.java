public class LinhaTabela {
    
    private String P, CX, CY;

    public LinhaTabela(String P) {
        this.P = P;
    }

    public String getP() {
        return P;
    }

    public void setP(String P) {
        this.P = P;
    }

    public String getCX() {
        return CX;
    }

    public void setCX(String CX) {
        this.CX = CX;
    }

    public String getCY() {
        return CY;
    }

    public void setCY(String CY) {
        this.CY = CY;
    }
    
    
    
}
